const MORSE_TABLE = {
  '.-': 'a',
  '-...': 'b',
  '-.-.': 'c',
  '-..': 'd',
  '.': 'e',
  '..-.': 'f',
  '--.': 'g',
  '....': 'h',
  '..': 'i',
  '.---': 'j',
  '-.-': 'k',
  '.-..': 'l',
  '--': 'm',
  '-.': 'n',
  '---': 'o',
  '.--.': 'p',
  '--.-': 'q',
  '.-.': 'r',
  '...': 's',
  '-': 't',
  '..-': 'u',
  '...-': 'v',
  '.--': 'w',
  '-..-': 'x',
  '-.--': 'y',
  '--..': 'z',
  '.----': '1',
  '..---': '2',
  '...--': '3',
  '....-': '4',
  '.....': '5',
  '-....': '6',
  '--...': '7',
  '---..': '8',
  '----.': '9',
  '-----': '0',
}

function decode(expr) {
  const arr = []

  for (let i = 0; i < expr.length - 1; i += 10) {
    expr.slice(i, i + 10) === '**********'
      ? arr.push(' ')
      : arr.push(
          MORSE_TABLE[
            expr
              .slice(i, i + 10)
              .replace(/00/gi, '')
              .replace(/11/gi, '-')
              .replace(/10/gi, '.')
          ]
        )
  }

  return arr.join('')
}

